def update_sitemap():
    """Add dream URLs to sitemap."""
    sitemap_path = os.path.join(os.path.dirname(os.path.dirname(__file__)), "public", "sitemap.xml")
    with open(sitemap_path, "r", encoding="utf-8") as f:
        content = f.read()

    # Check if dream URLs already exist
    if "/dream/" in content:
        print("Dream URLs already in sitemap, skipping.")
        return

    new_urls = f"""    <url>
        <loc>{SITE_URL}/dream/</loc>
        <lastmod>{TODAY}</lastmod>
        <changefreq>weekly</changefreq>
        <priority>0.9</priority>
    </url>"""

    for d in DREAMS:
        new_urls += f"""
    <url>
        <loc>{SITE_URL}/dream/{d['slug']}/</loc>
        <lastmod>{TODAY}</lastmod>
        <changefreq>monthly</changefreq>
        <priority>0.7</priority>
    </url>"""

    content = content.replace("</urlset>", new_urls + "\n</urlset>")
    with open(sitemap_path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"Added {len(DREAMS) + 1} dream URLs to sitemap.")


def main():
    # Create directories
    os.makedirs(BASE_DIR, exist_ok=True)

    # Generate hub page
    hub_html = generate_hub_page()
    hub_path = os.path.join(BASE_DIR, "index.html")
    with open(hub_path, "w", encoding="utf-8") as f:
        f.write(hub_html)
    print(f"Generated hub: {hub_path}")

    # Generate individual pages
    for dream in DREAMS:
        slug_dir = os.path.join(BASE_DIR, dream["slug"])
        os.makedirs(slug_dir, exist_ok=True)
        page_html = generate_page(dream)
        page_path = os.path.join(slug_dir, "index.html")
        with open(page_path, "w", encoding="utf-8") as f:
            f.write(page_html)
        print(f"Generated: /dream/{dream['slug']}/")

    print(f"\nTotal: {len(DREAMS)} dream pages + 1 hub page generated.")

    # Update sitemap
    update_sitemap()


if __name__ == "__main__":
    main()
