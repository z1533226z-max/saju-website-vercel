def update_sitemap():
    """sitemap.xml에 가이드 URL 추가"""
    sitemap_path = os.path.join(PUBLIC_DIR, "sitemap.xml")
    with open(sitemap_path, "r", encoding="utf-8") as f:
        content = f.read()

    # </urlset> 앞에 가이드 URL 삽입
    guide_urls = f"""    <url>
        <loc>https://saju.gon.ai.kr/guide/</loc>
        <lastmod>{TODAY}</lastmod>
        <changefreq>monthly</changefreq>
        <priority>0.9</priority>
    </url>
"""
    for g in GUIDES:
        guide_urls += f"""    <url>
        <loc>https://saju.gon.ai.kr/guide/{g['id']}/</loc>
        <lastmod>{TODAY}</lastmod>
        <changefreq>monthly</changefreq>
        <priority>0.8</priority>
    </url>
"""

    if "guide/" not in content:
        content = content.replace("</urlset>", guide_urls + "</urlset>")
        with open(sitemap_path, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"  ✅ sitemap.xml 업데이트 ({1 + len(GUIDES)}개 URL 추가)")
    else:
        print("  ⏭️ sitemap.xml에 이미 guide URL 존재")


